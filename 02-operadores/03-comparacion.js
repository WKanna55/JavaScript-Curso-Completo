// apra comparar distintas variables entre si
//  o una variable con otra cosa

let a = 10;
// operadores relacionales
console.log(a > 5); // nos da un boolean
console.log(a >= 5);  
console.log(a < 5);  
console.log(a <= 5);  

// operadores de igualdad
console.log(a == 10);
console.log(a != 10);
console.log(a == '10'); // da true? si los valores son iguales, no tipos
// preferible usar esto
console.log(a === '10'); // compara el tipo de dato
console.log(a !== '10'); // distinto tipo de dato
